let dia = 'SÁBADO'

switch (dia.toLowerCase()) {
    case 'segunda':
        console.log('Segunda é paia')
        break;
    case 'terça':
        console.log('Terça é dia de aula')
        break;
    case 'quarta':
        console.log('É quarta feira meus bacanas')
        break;
    case 'quinta':
        console.log('Quinta é dia de feira')
        break;
    case 'sexta':
        console.log('SEXTOU !!!')
        break;
    case 'sábado':
        console.log('Sábado é dia de relaxar')
        break;
    case 'domingo':
        console.log('Domingo é melancólico')
        break;
    default:
        break;
}


